CREATE TABLE CT_PHIEUKIEMKHO (
    MaPhieuKiemKho CHAR(8),
    MaBienThe CHAR(8),
    SoLuongHeThong INT NOT NULL,
    SoLuongThucTe INT NOT NULL,
    SoLuongChenhLech INT NOT NULL,
